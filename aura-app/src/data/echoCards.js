export function matchEchoes(entry, limit = 3) {
  const entryValence = getExpressionValence(entry);
  const entryDensity = entry.calendarDensity
    ? (entry.calendarDensity.eventCount > 4 ? 'high' : entry.calendarDensity.eventCount > 2 ? 'medium' : 'low')
    : 'medium';

  return echoCards
    .filter(e => e.tags.expressionValence === entryValence || e.tags.calendarDensity === entryDensity)
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
}

function getExpressionValence(entry) {
  if (!entry.colour) return 'medium';
  const hex = entry.colour.replace('#', '');
  const r = parseInt(hex.substr(0, 2), 16);
  const g = parseInt(hex.substr(2, 2), 16);
  const b = parseInt(hex.substr(4, 2), 16);
  const brightness = (r + g + b) / 3;
  if (brightness < 100) return 'low';
  if (brightness > 170) return 'high';
  return 'medium';
}
